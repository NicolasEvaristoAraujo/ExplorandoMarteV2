class sonda:

    __dir = 0
    __posX = 0
    __posY = 0
    

    def set_dir( direcao ):
        __dir = direcao

    def get_dir():
        return __dir

    def set_posX( posX ):
        __posX = posX

    def get_posX():
        return __posX

    def set_posY( posY ):
        __posY = posY

    def get_posY():
        return __posY
